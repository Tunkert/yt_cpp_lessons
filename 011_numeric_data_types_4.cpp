#include <iostream>

using namespace std;

int main() {
    // short ints
    cout << "The max value of a short int is " << INT16_MAX << ".\n";
    cout << "The min value of a short int is " << INT16_MIN << ".\n";
    // unsigned short int
    cout << "The max value of an unsigned short int is " << UINT16_MAX << ".\n";
    // long long int
    cout << "The max value of a long long int is " << INTMAX_MAX << ".\n";
    cout << "The min value of a long long int is " << INTMAX_MIN << ".\n";
    // unsigned long long int
    cout << "The max value of an unsigned long long int is " << UINTMAX_MAX << ".\n";
}