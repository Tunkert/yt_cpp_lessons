#include <iostream>

using namespace std;

int main() {
    int i = 0;
    cout << i++ << endl; // increment after console out
    cout << i << endl;
    cout << ++i << endl; // increment before console out
    cout << i << endl;
    cout << i-- << endl; // decrement after console out
    cout << i << endl;
    cout << --i << endl; // decrement before console out
    cout << i << endl;
}