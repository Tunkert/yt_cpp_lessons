#include <iostream>

using namespace std;

int main() {
    // these will return 1 if true, 0 if false
    cout << (5 > 2) << endl; // 1
    cout << (5 < 2) << endl; // 0
    cout << (5 >= 5) << endl; // 1
    cout << (5 > 5) << endl; // 0
    cout << (5 <= 5) << endl; // 1
    cout << (10 != 5) << endl; // 1
    cout << (10 == 5) << endl; // 0
}
